    private void TxtNomorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtNomorKeyReleased
            if (TxtNomor.getText()==""){
                JOptionPane.showMessageDialog(null,"No. Inventaris Belum Anda Masukkan");
            }else {
                String nomor=TxtNomor.getText();
                try{
                    Class.forName("com.mysql.jdbc.Driver");
                    Connection cn=DriverManager.getConnection("jdbc:mysql://localhost:3306/pln","root","root");
                    String sql = "SELECT * FROM tblbarang WHERE nomor='"+nomor+"'";
                    Statement st = cn.createStatement();
                    ResultSet rs = st.executeQuery(sql);
                    while (rs.next()){
                        JOptionPane.showMessageDialog(null,"No. Inventaris Sudah Anda Masukkan");
                        cbojenis.setSelectedItem(rs.getString("jenis"));
                        TxtNama.setText(rs.getString("nama"));
                        TxtTahun.setText(rs.getString("tahun"));
                        cboBagian.setSelectedItem(rs.getString("bagian"));
                        cboSeksi.setSelectedItem(rs.getString("seksi"));
                        cboRuang.setSelectedItem(rs.getString("ruang"));
                        cbokondisi.setSelectedItem(rs.getString("kondisi"));
                        TxtKeterangan.setText(rs.getString("keterangan"));
                        btnSimpan.setText("UPDATE");
                        btnSimpan.setMnemonic('U');
                        TxtNomor.setEnabled(false);
                        cbojenis.requestFocus();
                    }
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null,"Ada Kesalahan dalam Menyimpan Data Pada"+e,"Error",JOptionPane.INFORMATION_MESSAGE);

                }

        }
    }//GEN-LAST:event_TxtNomorKeyReleased

