public void loadData() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {            
            Connection c=ClassDB.getkoneksi();
            Statement s= c.createStatement();
            String sql="Select * from tblbarang";
            ResultSet r=s.executeQuery(sql);
            
            while (r.next()) {
                Object[] a=new Object[9];
                a[0]=r.getString("nomor");
                a[1]=r.getString("jenis");
                a[2]=r.getString("nama");
                a[3]=r.getString("tahun");
                a[4]=r.getString("bagian");
                a[5]=r.getString("seksi");
                a[6]=r.getString("ruang");
                a[7]=r.getString("kondisi");
                a[8]=r.getString("keterangan");
                model.addRow(a);
            }
            r.close();
            s.close();
            ShowData();
        }catch(SQLException e) {
            System.out.println("Terjadi kesalahan");
        }
    }

