    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String nomor="", jenis="", nama="", tahun="", bagian="", seksi="", ruang="", kondisi="",keterangan="",sql="";
        nomor=TxtNomor.getText();
        nama=TxtNama.getText();
        keterangan =TxtKeterangan.getText();
    if ((nomor.isEmpty()) | (nama.isEmpty())| (keterangan.isEmpty()))
       {JOptionPane.showMessageDialog(null,"Data Tidak Boleh Kosong, Silahkan Dilengkapi");
        TxtNomor.requestFocus();
    }
else{
        try{
            nomor=TxtNomor.getText();
            jenis=cbojenis.getSelectedItem().toString();
            nama=TxtNama.getText();
            tahun=(TxtTahun.getText());
            bagian=cboBagian.getSelectedItem().toString();
            seksi=cboSeksi.getSelectedItem().toString();
            ruang=cboRuang.getSelectedItem().toString();
            kondisi =cbokondisi.getSelectedItem().toString();
            keterangan =TxtKeterangan.getText();
            Class.forName("com.mysql.jdbc.Driver");
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/pln","root","root");

            if (btnSimpan.getText()=="Simpan") {
                sql="Insert into tblbarang values('"+nomor+"','"+jenis+"','"+nama+"', '"+tahun+"', '"+bagian+"', '"+seksi+"', '"+ruang+"', '"+kondisi+"', '"+keterangan+"')";
                Statement st=cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null,"Data Sudah Disimpan","SIMPAN",JOptionPane.INFORMATION_MESSAGE);
            } else if (btnSimpan.getText()=="Update") {
                sql="UPDATE tblbarang SET jenis=?, nama=?, tahun=?, bagian=?, seksi=?, ruang=?, kondisi=?, keterangan=? WHERE nomor='"+ nomor +"'";
                //Statement st=cn.createStatement();
                PreparedStatement st=(PreparedStatement) cn.prepareStatement(sql);
                try {
                    st.setString(1, cbojenis.getSelectedItem().toString());
                    st.setString(2, TxtNama.getText());
                    st.setString(3, TxtTahun.getText());
                    st.setString(4, cboBagian.getSelectedItem().toString());
                    st.setString(5, cboSeksi.getSelectedItem().toString());
                    st.setString(6, cboRuang.getSelectedItem().toString());
                    st.setString(7, cbokondisi.getSelectedItem().toString());
                    st.setString(8, TxtKeterangan.getText());
                    st.executeUpdate();
                    JOptionPane.showMessageDialog(null,"Data Berhasil di Update","UPDATE",JOptionPane.INFORMATION_MESSAGE);

                }catch(SQLException se) {
                    JOptionPane.showMessageDialog(null,"Ada Kesalahan Dalam Update Data"+se,"Error",JOptionPane.YES_OPTION);
                }
            }
            BersihText();
            aktifText(false);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Ada Kesalahan dalam Menyimpan Data Pada :"+e,"Error",JOptionPane.INFORMATION_MESSAGE);
        }
                loadData();
    }//GEN-LAST:event_btnSimpanActionPerformed
    }
    
