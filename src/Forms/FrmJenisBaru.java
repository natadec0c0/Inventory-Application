    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
       String kode=TxtKode.getText();
       String jenis=TxtNama.getText();

        if (jenis.isEmpty()|kode.isEmpty())
       {
           JOptionPane.showMessageDialog(null,"Anda belum menentukan pilihan.");
       }
        else{
        int tanya=JOptionPane.showConfirmDialog(null,"Yakin Jenis Inventaris : "+ TxtNama.getText()+" Akan Dihapus?","Hapus",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if (tanya==0)

            try{
                Class.forName("com.mysql.jdbc.Driver");
                Connection cn=DriverManager.getConnection("jdbc:mysql://localhost:3306/pln","root","root");
                String sql="DELETE FROM tbljenis WHERE jenis='"+TxtNama.getText()+"'";
                Statement st=cn.createStatement();
                st. executeUpdate(sql);
                JOptionPane.showMessageDialog(null,"Hapus Data Berhasil...","Sukses",JOptionPane.INFORMATION_MESSAGE);
                BersihText();
                aktifText(false);
            }

            catch(Exception e) {
                JOptionPane.showMessageDialog(null,"Ada Kesalahan dalam Menyimpan Data Pada :"+e,"Error",JOptionPane.INFORMATION_MESSAGE);
            } finally{
                loadData();
            }
        }
}//GEN-LAST:event_btnHapusActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String kode="",jenis="",sql="";
        try{
       kode=TxtKode.getText();
       jenis=TxtNama.getText();
       if (jenis.isEmpty()|kode.isEmpty())
       {
           JOptionPane.showMessageDialog(null,"Data Masih Ada Yang Kosong, Silahkan Dilengkapi");
           TxtKode.requestFocus();
       }
        else{

            Class.forName("com.mysql.jdbc.Driver");
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/pln","root","root");
            if (btnSimpan.getText()=="Simpan") {
                //Mengambil data yg dimasukkan dari JTextField
                sql="Insert into tbljenis values('"+kode+"','"+jenis+"')";
                Statement st=cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null,"Data Sudah Disimpan","Simpan",JOptionPane.INFORMATION_MESSAGE);
            } else if (btnSimpan.getText()=="Update") {
                sql="UPDATE tbljenis SET jenis=?  WHERE kode='"+ kode +"'";
                //Statement st=cn.createStatement();
                PreparedStatement st=(PreparedStatement) cn.prepareStatement(sql);
                try {
                    st.setString(1, TxtNama.getText());
                    st.executeUpdate();
                    JOptionPane.showMessageDialog(null,"Data Berhasil di Update","Update",JOptionPane.INFORMATION_MESSAGE);
}catch(SQLException se) {
                    JOptionPane.showMessageDialog(null,"Ada Kesalahan Dalam Update Data"+se,"Error",JOptionPane.YES_OPTION);
                }
            }
            BersihText();
            aktifText(false);
    }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Ada Kesalahan dalam Menyimpan Data Pada :"+e,"Error",JOptionPane.INFORMATION_MESSAGE);
        }
        loadData();
}//GEN-LAST:event_btnSimpanActionPerformed

