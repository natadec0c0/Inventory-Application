    private void TxtKodeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtKodeKeyReleased
            if (TxtKode.getText()==""){
                JOptionPane.showMessageDialog(null,"No. Jenis Inventaris Belum Anda Masukkan");
            }else {
                String kode=TxtKode.getText();
                try{
                    Class.forName("com.mysql.jdbc.Driver");
                    Connection cn=DriverManager.getConnection("jdbc:mysql://localhost:3306/pln","root","root");
                    String sql = "SELECT*FROM tbljenis WHERE kode='"+kode+"'";
                    Statement st = cn.createStatement();
                    ResultSet rs = st.executeQuery(sql);
                    while (rs.next()){
                        JOptionPane.showMessageDialog(null,"No. Inventaris Sudah Anda Masukkan");
                        TxtNama.setText(rs.getString("jenis"));
                        btnSimpan.setText("Update");
                        btnSimpan.setMnemonic('U');
                        TxtKode.setEnabled(false);
                        TxtNama.requestFocus();
                    }
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null,"Ada Kesalahan dalam Menyimpan Data Pada"+e,"Error",JOptionPane.INFORMATION_MESSAGE);

                }

        }
    }//GEN-LAST:event_TxtKodeKeyReleased

